function showStudyRoom() {
    const gameContent = document.getElementById('game-content');
    const target = gameContent || document.body;

    if (!document.querySelector('.study-room')) {
        const studyRoom = document.createElement('div');
        studyRoom.className = 'study-room';

        const table = document.createElement('div');
        table.className = 'study-table';
        table.setAttribute('data-text', '大学知识前瞻');
        table.addEventListener('click', function() {
            handleTableClick();
        });
        studyRoom.appendChild(table);

        const npcStudy = document.createElement('div');
        npcStudy.className = 'study-npc';
        npcStudy.id = 'study-npc';
        npcStudy.innerHTML = '<div class="npc-bubble"></div>';
        npcStudy.addEventListener('click', function(e) {
            e.stopPropagation();
            handleStudyNPCClick();
        });
        studyRoom.appendChild(npcStudy);

        target.appendChild(studyRoom);

        showStudyNPCBubble();
    }
}

function showStudyNPCBubble() {
    const npc = document.getElementById('study-npc');
    if (!npc) return;

    const bubble = npc.querySelector('.npc-bubble');
    if (!bubble) return;

    if (studyNPCState.hideTimer) clearTimeout(studyNPCState.hideTimer);

    bubble.textContent = studyNPCDialogues[studyNPCState.index];
    bubble.classList.remove('show', 'hide');
    void bubble.offsetWidth;
    bubble.classList.add('show');

    studyNPCState.hideTimer = setTimeout(() => {
        bubble.classList.remove('show');
        bubble.classList.add('hide');
    }, 12000);
}

function handleStudyNPCClick() {
    if (studyNPCState.bubbleTimer) clearTimeout(studyNPCState.bubbleTimer);
    if (studyNPCState.hideTimer) clearTimeout(studyNPCState.hideTimer);

    studyNPCState.index = (studyNPCState.index + 1) % studyNPCDialogues.length;
    showStudyNPCBubble();
}

