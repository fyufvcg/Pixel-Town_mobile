function showUniversityKnowledgeWindow() {
    const existingWindow = document.getElementById('university-knowledge-window');
    if (existingWindow) {
        existingWindow.remove();
        return;
    }

    const window = document.createElement('div');
    window.className = 'university-knowledge-window';
    window.id = 'university-knowledge-window';

    const userId = localStorage.getItem('pixelTownUserId');
    
    const loadingHTML = '<div class="university-loading">正在加载知识标签...</div>';
    
    window.innerHTML = `
        <div class="university-window-content">
            <div class="university-window-header">
                <div class="university-window-title">大学知识前瞻</div>
                <button class="university-window-close" onclick="document.getElementById('university-knowledge-window').remove()"><span>×</span></button>
            </div>
            <div class="university-window-body">
                <div class="university-window-tags" id="university-tags">${loadingHTML}</div>
                <div class="university-window-content-inner" id="university-content"></div>
            </div>
        </div>
    `;

    const gameContent = document.getElementById('game-content');
    if (gameContent) {
        gameContent.appendChild(window);
    } else {
        document.body.appendChild(window);
    }

    fetch(`http://47.98.245.103:5000/get_user_tags?user_id=${userId}`)
        .then(res => res.json())
        .then(data => {
            const userTags = [];
            if (data.data && Array.isArray(data.data)) {
                data.data.forEach(t => {
                    if (universityKnowledgeData[t.tag_name] && t.wrong_count > 0) {
                        userTags.push({ name: t.tag_name, wrongCount: t.wrong_count || 0 });
                    }
                });
            }
            
            userTags.sort((a, b) => a.wrongCount - b.wrongCount);
            
            renderUniversityTags(userTags);
        })
        .catch(() => {
            renderUniversityTags([]);
        });

    setTimeout(function() {
        window.classList.add('show');
    }, 10);
}

function renderUniversityTags(userTags) {
    const tagsContainer = document.getElementById('university-tags');
    const contentContainer = document.getElementById('university-content');
    
    if (!tagsContainer || !contentContainer) return;
    
    if (userTags.length === 0) {
        tagsContainer.innerHTML = '<div class="university-window-empty">暂无学习记录，快去做题积累错误吧！</div>';
        contentContainer.innerHTML = '<div class="university-window-empty">完成关卡题目后，这里将显示您的薄弱知识点对应的大学拓展知识。</div>';
        return;
    }
    
    let tagsHTML = '';
    userTags.forEach((tag, index) => {
        tagsHTML += `<button class="university-window-tag ${index === 0 ? 'active' : ''}" onclick="showUniversityContent('${tag.name}', this)">${tag.name}</button>`;
    });
    tagsContainer.innerHTML = tagsHTML;
    
    if (userTags.length > 0) {
        showUniversityContent(userTags[0].name, tagsContainer.querySelector('.university-window-tag'));
    }
}

function showUniversityContent(tag, btn) {
    const contentDiv = document.getElementById('university-content');
    if (!contentDiv) return;

    const content = universityKnowledgeData[tag];
    if (content) {
        contentDiv.innerHTML = content;
    }

    document.querySelectorAll('.university-window-tag').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
}

