// 根据ID获取文章
function getArticleById(category, id) {
    const articles = getArticlesByCategory(category);
    return articles.find(article => article.id === id);
}

// 检查答案
function checkArticleAnswer(category, id, userAnswer) {
    const article = getArticleById(category, id);
    if (!article) return { correct: false, message: "文章不存在" };
    
    const correct = userAnswer === article.correctAnswer;
    return {
        correct: correct,
        correctAnswer: article.correctAnswer,
        message: correct ? "回答正确！" : "回答错误，正确答案是 " + article.correctAnswer
    };
}